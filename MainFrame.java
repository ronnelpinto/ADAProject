    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        //decrypt button
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please choose a file to decrypt.");
        }else
        if(jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please choose a private key for decryption.");
        }else{
            jTextArea2.setText("");
            String msg = "";
            boolean flag = true;
            try {
                msg = Connector.decrypt(jTextField2.getText(), jTextField3.getText());
            } catch (    ClassNotFoundException | IOException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                flag = false;
            } catch( NumberFormatException e){
                flag = false;
            }
            if(flag){
                jTextArea2.setText(msg);
                JOptionPane.showMessageDialog(this, "Message decrypted.");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Wrong inputs. Please try again.");
            }
        }
    }//GEN-LAST:event_jButton5MouseClicked

