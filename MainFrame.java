    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        //encrypt button
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please choose a public key.");
        }else
        if(jTextField5.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please choose a destination to save the encrypted file.");
        }else
        if(jTextArea1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Empty message field.Please fill in the message to encrypt.");
        }else{
            try {
                Connector.encrypt(jTextField1.getText(),jTextField5.getText(),jTextArea1.getText());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (    IOException | ClassNotFoundException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextArea1.setText("");
            JOptionPane.showMessageDialog(this, "Message Encrypted.");
        }
    }//GEN-LAST:event_jButton1MouseClicked

